/* Andy
	Tera Forest Time Gate
 */

var status;

function start() {
    status = -1;
    action(1, 0, 0);
}

function action(mode, type, selection) {
    if (mode == -1) {
        cm.dispose();
    } else {
        if (mode == 0 && type > 0) {
            cm.dispose();
            return;
        }
        if (mode == 1) {
            status++;
        } else {
            status--;
        }

        if (status == 0) {
            cm.sendOk("嗨，我是安迪，来自不远的未来的时间旅行者。我是来阻止贪婪的人们创造机器的。他们对我的时间发狂，把一切都化为灰烬。我必须不惜一切代价阻止它！");
            cm.dispose();
        }
    }
}
